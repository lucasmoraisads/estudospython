filme = "Esquadrão classe a"
# string [inicio:fim] indice começa 0 e termina com indice -1
# Buscar toda string a parti de primeira posição

print(filme[0:])

#buscar toda a string ate a ultima posição

print(filme[:16])

# buscar toda a string começando da 3 ate a ultima

print(filme[3:])

'''
# string [inicio:fim:passo] indice começa 0 e termina com indice -1
'''
#buscar toda a string 2 em 2 caracteres

print(filme[::2])

#buscar toda a string nos numeros impas

print(filme[1::2])

#inverter uma string de tras para a frente

print(filme[::-1])